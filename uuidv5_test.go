package uuid

import (
	"testing"
)

func TestNewV5(t *testing.T) {
	idv5 := UUID{0x36, 0x6a, 0xb4, 0xa5, 0x4c, 0x49, 0x5e, 0xbe, 0xb5, 0x3b, 0x13, 0xe0, 0x42, 0x94, 0x6d, 0xee}
	id, _ := NewV5(UUID{0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF, 0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF}, "test")
	if id != idv5 {
		t.Errorf("uuid.NewV5() = %v, want %v", id, idv5)
	}
}
