package uuid

import (
	"testing"
)

func TestNewV8(t *testing.T) {
	want := UUID{0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0x8D, 0xEF, 0x81, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF}
	id := NewV8([]byte{0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF, 0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF})

	if id != want {
		t.Errorf("uuid.NewV8() = %v, want %v", id, want)
	}
}
