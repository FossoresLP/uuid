package uuid

import (
	"testing"
)

func TestNewV3(t *testing.T) {
	idv3 := UUID{0xee, 0x5b, 0x0c, 0xe6, 0x42, 0xd1, 0x34, 0x84, 0xad, 0x99, 0x20, 0xf2, 0x6c, 0x2a, 0x8f, 0xae}
	id, _ := NewV3(UUID{0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF, 0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF}, "test")
	if id != idv3 {
		t.Errorf("uuid.NewV3() = %v, want %v", id, idv3)
	}
}
